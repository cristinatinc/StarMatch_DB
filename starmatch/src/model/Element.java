package model;

/**
 * Represents the four classical elements: Fire, Water, Air, and Earth.
 * These elements can be used to classify objects or attributes in the
 * context of astrology.
 */
public enum Element {
    /**
     * Represents the Fire element.
     */
    Fire,
    /**
     * Represents the Water element.
     */
    Water,
    /**
     * Represents the Air element.
     */
    Air,
    /**
     * Represents the Earth element.
     */
    Earth
}
